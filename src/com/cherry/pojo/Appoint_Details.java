package com.cherry.pojo;

public class Appoint_Details 
{
 private int ap_id;
 private int pid;
 private int d_id;
 private String date;
 private String disease;
public int getAp_id() {
	return ap_id;
}
public void setAp_id(int ap_id) {
	this.ap_id = ap_id;
}
public int getPid() {
	return pid;
}
public void setPid(int pid) {
	this.pid = pid;
}
public int getD_id() {
	return d_id;
}
public void setD_id(int d_id) {
	this.d_id = d_id;
}
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}
public String getDisease() {
	return disease;
}
public void setDisease(String disease) {
	this.disease = disease;
}

 
}
